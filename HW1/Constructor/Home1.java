package HW1.Constructor;

public class Home1 {
     String city;
     int cost ;

     // No Parameter constructor
     Home1(){

     }

     // For constructor, we don't have any return Type
     // we can Also Set the values in Constructor
     // This is Parameterised Constructor
     public Home1(String city,int cost){
          this.city=city;
          this.cost=cost;
     }

}
